package olc1.practica1_201700371;

/**
 *
 * @author mynor
 */
public class MacroConjunto {

    String id;
    String macro;

    public MacroConjunto(String id, String macro) {
        this.id = id;
        this.macro = macro;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMacro() {
        return macro;
    }

    public void setMacro(String macro) {
        this.macro = macro;
    }
    
    
    
}
